public class InDec {
    public static void main(String[] args) {

        /*
        Post increment and Pre increment

        int x = 5, y;

        // y = x++; // post increment
        y = ++x; // pre increment
        System.out.println(x + " " + y);
         */

        /*
        Expression using Increment

         int x = 5, y = 4, z;

        z = 2 * x++ + 3 * ++y; // Also works for byte, short, long, float, char

        System.out.println(z + " " + x + " " + y);
         */

    }
}
